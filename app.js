function setCookieToggles(value) {
  document.querySelectorAll("[data-cookie-toggle]").forEach((input) => {
    input.checked = Boolean(value[input.dataset.cookieToggle]);
  });
}

function wireCookies() {
  const banner = document.querySelector("#cookieBanner");
  const modal = document.querySelector("#cookieModal");
  const saved = localStorage.getItem(keys.cookies);

  if (!saved) {
    banner.hidden = false;
  } else {
    try {
      setCookieToggles(JSON.parse(saved));
    } catch {
      banner.hidden = false;
    }
  }

  document.querySelectorAll("[data-open-cookies]").forEach((button) => {
    button.addEventListener("click", () => {
      modal.hidden = false;
    });
  });

  document.querySelector("#closeCookies").addEventListener("click", () => {
    modal.hidden = true;
  });

  document.querySelector("#acceptCookies").addEventListener("click", () => {
    cookieState({ necessary: true, analytics: true, personalization: true, marketing: true });
  });

  document.querySelector("#rejectCookies").addEventListener("click", () => {
    cookieState({ necessary: true, analytics: false, personalization: false, marketing: false });
  });

  document.querySelector("#saveReject").addEventListener("click", () => {
    setCookieToggles({});
    cookieState({ necessary: true, analytics: false, personalization: false, marketing: false });
  });

  document.querySelector("#saveSelection").addEventListener("click", () => {
    cookieState(getCookieSelection());
  });

  document.querySelector("#saveAll").addEventListener("click", () => {
    setCookieToggles({ analytics: true, personalization: true, marketing: true });
    cookieState({ necessary: true, analytics: true, personalization: true, marketing: true });
  });
}

function wireBookingNotice() {
  const modal = document.querySelector("#bookingNoticeModal");
  const close = document.querySelector("#closeBookingNotice");
  const continueButton = document.querySelector("#continueBooking");
  let pendingHref = "";
  let pendingTarget = "";

  if (!modal || !close || !continueButton) return;

  const hide = () => {
    modal.hidden = true;
  };

  document.addEventListener("click", (event) => {
    const link = event.target.closest("a");
    if (!link) return;

    const href = link.getAttribute("href") || "";
    const isBookingLink = href === "#booking" || href.includes("planity.com/lazoya-17000-la-rochelle");
    if (!isBookingLink || link.dataset.bookingNoticeSeen === "true") return;

    event.preventDefault();
    pendingHref = href;
    pendingTarget = link.getAttribute("target") || "";
    modal.hidden = false;
  });

  close.addEventListener("click", hide);

  modal.addEventListener("click", (event) => {
    if (event.target === modal) hide();
  });

  continueButton.addEventListener("click", () => {
    hide();
    if (!pendingHref) return;

    if (pendingHref.startsWith("#")) {
      window.location.hash = pendingHref.slice(1);
      return;
    }

    window.open(pendingHref, pendingTarget === "_blank" ? "_blank" : "_self", "noreferrer");
  });
}

function wireSuccessModal() {
  const modal = document.querySelector("#successModal");
  const close = document.querySelector("#closeSuccess");
  const ok = document.querySelector("#successOk");

  if (!modal || !close || !ok) return;

  const hide = () => {
    modal.hidden = true;
  };

  close.addEventListener("click", hide);
  ok.addEventListener("click", hide);
  modal.addEventListener("click", (event) => {
    if (event.target === modal) hide();
  });
}

document.querySelector("#resetDemo").addEventListener("click", () => {
  localStorage.removeItem(keys.services);
  localStorage.removeItem(keys.products);
  state.services = structuredClone(defaults.services);
  state.products = structuredClone(defaults.products);
  state.serviceFilter = "all";
  state.serviceQuery = "";
  state.productQuery = "";
  document.querySelector("#serviceSearch").value = "";
  const productSearch = document.querySelector("#productSearch");
  if (productSearch) productSearch.value = "";
  renderServices();
  renderProducts();
  showToast("Contenu réinitialisé.");
});

