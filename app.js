function wireNavigation() {
  const setRoute = () => {
    const rawRoute = window.location.hash.replace("#", "") || "home";
    const route = routeAliases[rawRoute] || rawRoute;
    const view = views.has(route) ? route : "home";

    document.body.dataset.view = view;
    topbar.dataset.elevated = String(view !== "home" || window.scrollY > 20);
    document.querySelectorAll(".nav-links a").forEach((link) => {
      link.classList.toggle("active", link.getAttribute("href") === `#${view}`);
    });

    if (rawRoute === "careerForm") {
      window.setTimeout(() => document.querySelector("#careerForm").scrollIntoView({ block: "start" }), 0);
    } else {
      window.scrollTo({ top: 0, behavior: "auto" });
      window.setTimeout(() => window.scrollTo({ top: 0, behavior: "auto" }), 0);
    }
  };

  setRoute();
  window.addEventListener("hashchange", setRoute);

  window.addEventListener("scroll", () => {
    topbar.dataset.elevated = String(document.body.dataset.view !== "home" || window.scrollY > 20);
  });

  menuButton.addEventListener("click", () => {
    const open = menuButton.getAttribute("aria-expanded") === "true";
    menuButton.setAttribute("aria-expanded", String(!open));
    topbar.classList.toggle("menu-active", !open);
    document.body.classList.toggle("menu-open", !open);
  });

  document.querySelectorAll(".nav-links a, .nav-actions a").forEach((link) => {
    link.addEventListener("click", () => {
      menuButton.setAttribute("aria-expanded", "false");
      topbar.classList.remove("menu-active");
      document.body.classList.remove("menu-open");
    });
  });

  document.addEventListener("click", (event) => {
    const serviceLink = event.target.closest("[data-service-category]");
    if (!serviceLink) return;

    event.preventDefault();
    state.serviceFilter = serviceLink.dataset.serviceCategory;
    state.serviceQuery = "";
    const searchInput = document.querySelector("#serviceSearch");
    if (searchInput) {
      searchInput.value = "";
    }
    renderServices();

    if (window.location.hash === "#services") {
      setRoute();
    } else {
      window.location.hash = "services";
    }
  });

  document.addEventListener("keydown", (event) => {
    if (event.key !== "Enter" && event.key !== " ") return;
    const serviceCard = event.target.closest(".home-services [data-service-category]");
    if (!serviceCard) return;

    event.preventDefault();
    serviceCard.click();
  });
}

function wireReviewSlider() {
  const slider = document.querySelector("[data-review-slider]");
  if (!slider) return;

  const slides = [...slider.querySelectorAll("[data-review-slide]")];
  const dots = [...slider.querySelectorAll("[data-review-dot]")];
  const previous = slider.querySelector("[data-review-prev]");
  const next = slider.querySelector("[data-review-next]");
  let active = 0;
  let timer;
  const visibleCount = () => (window.matchMedia("(max-width: 720px)").matches ? 1 : window.matchMedia("(max-width: 1040px)").matches ? 2 : 3);
  const pageCount = () => slides.length;

  const show = (index) => {
    active = (index + pageCount()) % pageCount();
    const visibleIndexes = Array.from({ length: visibleCount() }, (_, slot) => (active + slot) % slides.length);
    slides.forEach((slide, slideIndex) => {
      const slot = visibleIndexes.indexOf(slideIndex);
      slide.classList.toggle("active", slot !== -1);
      slide.style.order = slot === -1 ? "" : String(slot);
    });
    dots.forEach((dot, dotIndex) => {
      dot.classList.toggle("active", dotIndex === active);
      dot.hidden = dotIndex >= pageCount();
    });
  };

  const restart = () => {
    window.clearInterval(timer);
    timer = window.setInterval(() => show(active + 1), 5200);
  };

  previous?.addEventListener("click", () => {
    show(active - 1);
    restart();
  });

  next?.addEventListener("click", () => {
    show(active + 1);
    restart();
  });

  dots.forEach((dot) => {
    dot.addEventListener("click", () => {
      show(Number(dot.dataset.reviewDot));
      restart();
    });
  });

  window.addEventListener("resize", () => show(active));
  show(0);
  restart();
}

